#include <so_long.h>

void	ft_post(char **post, int n)
{
	if (*post)
	{
		free(*post);
		*post = NULL;
	}
	*post = ft_itoa(n);
	if (n < 10)
		*post = ft_strjoinf("0", *post, 2);
}
